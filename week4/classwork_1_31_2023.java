package week4;

public class classwork_1_31_2023 {
    public static void main(String[] args)
    {
        String name = "YourName";
        int y = 0;
        int x = 0;
        // 3 types of loops
        for (int i = 0; i <= 1000; i++)
        {
            System.out.println(name);
        }
        do{
            System.out.println(name);
            y++;
        } while (y <= 1000);
        while (x <= 1000)
        {
            System.out.println(name);
            x++;
        }
    }
}
